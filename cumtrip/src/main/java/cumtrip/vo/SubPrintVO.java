package cumtrip.vo;

public class SubPrintVO {
	private String main_cate;
	private String mid_name;
	private String photo_path;
	private String mid_no;
	
	public String getMain_cate() {
		return main_cate;
	}
	public void setMain_cate(String main_cate) {
		this.main_cate = main_cate;
	}
	public String getMid_name() {
		return mid_name;
	}
	public void setMid_name(String mid_name) {
		this.mid_name = mid_name;
	}
	public String getPhoto_path() {
		return photo_path;
	}
	public void setPhoto_path(String photo_path) {
		this.photo_path = photo_path;
	}
	public String getMid_no() {
		return mid_no;
	}
	public void setMid_no(String mid_no) {
		this.mid_no = mid_no;
	}
	
}
